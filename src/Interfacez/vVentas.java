    void showNewModule(Integer indexModule){
    
        switch(indexModule){
        
            case 0: // proveedor
                Proveedores pp = new Proveedores(lProveedores,this);
                pp.setVisible(true);
                break;
            case 1:// cliente
                clientePane cp = new clientePane(lClientes);
                cp.setVisible(true);
                break;
            case 2: // vendedor
                VendedoresFrame vp = new VendedoresFrame(lVendedor,this);
                vp.setVisible(true);
                break;
            case 3:
                 productoPane pProducto = new productoPane(lProveedores, this);
                 pProducto.setVisible(true);
                 break;
        }
        
    }
    
